package com.isk.xtractor.model.entity;

public class Environment {

	private String envName;
	private String envURL;
	private String envPORT;
	private String envUSERNAME;
	private String envPASSWORD;
	private String envLogPath;

	public String getEnvName() {
		return envName;
	}

	public void setEnvName(String envName) {
		this.envName = envName;
	}

	public String getEnvURL() {
		return envURL;
	}

	public void setEnvURL(String envURL) {
		this.envURL = envURL;
	}

	public String getEnvPORT() {
		return envPORT;
	}

	public void setEnvPORT(String envPORT) {
		this.envPORT = envPORT;
	}

	public String getEnvUSERNAME() {
		return envUSERNAME;
	}

	public void setEnvUSERNAME(String envUSERNAME) {
		this.envUSERNAME = envUSERNAME;
	}

	public String getEnvPASSWORD() {
		return envPASSWORD;
	}

	public void setEnvPASSWORD(String envPASSWORD) {
		this.envPASSWORD = envPASSWORD;
	}

	public String getEnvLogPath() {
		return envLogPath;
	}

	public void setEnvLogPath(String envLogPath) {
		this.envLogPath = envLogPath;
	}

}
